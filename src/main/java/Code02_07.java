public class Code02_07 {
    public static void main(String[] args) {
        int num1, num2, resuit;
        num1 = 100;
        num2 = 50;

        resuit = num1 + num2;
        System.out.println(num1 + "+" + num2 + "=" + resuit);

        resuit = num1 - num2;
        System.out.println(num1 + "-" + num2 + "=" + resuit);

        resuit = num1 * num2;
        System.out.println(num1 + "*" + num2 + "=" + resuit);

        resuit =num1 / num2;
        System.out.println(num1 + "/" + num2 + "=" + resuit);
    }
}
